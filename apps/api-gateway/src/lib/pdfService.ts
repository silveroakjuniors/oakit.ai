/** Collect all pdfkit chunks into a single Buffer via a Promise. */
function collectBuffer(doc: PDFKit.PDFDocument): Promise<Buffer> {
  return new Promise((resolve, reject) => {
    const chunks: Buffer[] = [];
    doc.on('data', (chunk: Buffer) => chunks.push(chunk));
    doc.on('end', () => resolve(Buffer.concat(chunks)));
    doc.on('error', reject);
  });
}

const MARGIN = 50;
const PAGE_WIDTH = 595.28; // A4 width in points
const CONTENT_WIDTH = PAGE_WIDTH - MARGIN * 2;

/**
 * Add a branded header to the current page position.
 * TODO: Add logo rendering once logo_url is a reliable local/CDN path.
 *       Currently skipped because logo_url may be a remote URL that requires
 *       async fetching; use school name as text header instead.
 */
function addBrandedHeader(
  doc: PDFKit.PDFDocument,
  branding: BrandingContext,
  reportTitle?: string,
): void {
  const startY = doc.y;

  // School name — bold, 16pt, centered
  doc
    .font('Helvetica-Bold')
    .fontSize(16)
    .text(branding.school_name, MARGIN, startY, { align: 'center', width: CONTENT_WIDTH });

  // School address — 10pt, gray, centered
  doc
    .font('Helvetica')
    .fontSize(10)
    .fillColor('#666666')
    .text(branding.school_address, MARGIN, doc.y + 2, { align: 'center', width: CONTENT_WIDTH });

  // Optional report title
  if (reportTitle) {
    doc
      .font('Helvetica-Bold')
      .fontSize(12)
      .fillColor('#000000')
      .text(reportTitle, MARGIN, doc.y + 4, { align: 'center', width: CONTENT_WIDTH });
  }

  // Reset fill color
  doc.fillColor('#000000');

  // Horizontal rule
  const ruleY = doc.y + 6;
  doc
    .moveTo(MARGIN, ruleY)
    .lineTo(PAGE_WIDTH - MARGIN, ruleY)
    .strokeColor('#cccccc')
    .lineWidth(1)
    .stroke();

  doc.strokeColor('#000000').lineWidth(1);
  doc.moveDown(0.5);
}

/**
 * Register a footer on every page that shows:
 * "Generated by [name] ([role]) | Page X of Y | Generated on [date]"
 *
 * pdfkit does not natively support "Page X of Y" without a two-pass render,
 * so we use a range object and update it after all pages are added.
 */
function registerFooter(
  doc: PDFKit.PDFDocument,
  ctx: GeneratorContext,
): void {
  const dateStr = ctx.generated_at.toLocaleDateString('en-IN', {
    day: '2-digit',
    month: 'short',
    year: 'numeric',
  });

  let pageNumber = 0;

  doc.on('pageAdded', () => {
    pageNumber++;
    // We write the footer at the bottom of each page as it is added.
    // "Page X of Y" — total pages unknown at this point, so we write "Page X"
    // and append "of Y" in the finalise step via the bufferedPageRange trick.
    const footerY = doc.page.height - 40;
    const footerText = `Generated by ${ctx.generated_by_name} (${ctx.generated_by_role}) | Page ${pageNumber} | Generated on ${dateStr}`;

    doc
      .font('Helvetica')
      .fontSize(8)
      .fillColor('#888888')
      .text(footerText, MARGIN, footerY, { align: 'center', width: CONTENT_WIDTH });

    doc.fillColor('#000000');
  });
}

/** Draw a simple two-column key-value row. */
function addKeyValue(
  doc: PDFKit.PDFDocument,
  label: string,
  value: string,
  y?: number,
): void {
  const rowY = y ?? doc.y;
  doc
    .font('Helvetica-Bold')
    .fontSize(10)
    .text(label + ':', MARGIN, rowY, { continued: false, width: 160 });
  doc
    .font('Helvetica')
    .fontSize(10)
    .text(value, MARGIN + 170, rowY, { width: CONTENT_WIDTH - 170 });
}

/** Draw a simple table with headers and rows. */
function addTable(
  doc: PDFKit.PDFDocument,
  headers: string[],
  rows: string[][],
  colWidths?: number[],
): void {
  const defaultColWidth = CONTENT_WIDTH / headers.length;
  const widths = colWidths ?? headers.map(() => defaultColWidth);

  const rowHeight = 20;
  let x = MARGIN;
  let y = doc.y + 4;

  // Header row background
  doc.rect(MARGIN, y, CONTENT_WIDTH, rowHeight).fill('#f0f0f0');
  doc.fillColor('#000000');

  headers.forEach((header, i) => {
    doc
      .font('Helvetica-Bold')
      .fontSize(9)
      .text(header, x + 4, y + 5, { width: widths[i] - 8, lineBreak: false });
    x += widths[i];
  });

  y += rowHeight;

  rows.forEach((row, rowIdx) => {
    // Alternate row shading
    if (rowIdx % 2 === 1) {
      doc.rect(MARGIN, y, CONTENT_WIDTH, rowHeight).fill('#fafafa');
      doc.fillColor('#000000');
    }

    x = MARGIN;
    row.forEach((cell, i) => {
      doc
        .font('Helvetica')
        .fontSize(9)
        .text(cell, x + 4, y + 5, { width: widths[i] - 8, lineBreak: false });
      x += widths[i];
    });

    y += rowHeight;

    // Add a new page if we're running out of space
    if (y > doc.page.height - 80) {
      doc.addPage();
      y = doc.y;
    }
  });

  doc.y = y + 4;
}

