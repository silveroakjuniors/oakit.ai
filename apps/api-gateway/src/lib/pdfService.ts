/**
 * Generate a fee payment receipt PDF.
 */
export async function generateReceiptPDF(
  data: ReceiptData,
  branding: BrandingContext,
  ctx: GeneratorContext,
): Promise<Buffer> {
  const doc = new PDFDocument({ margin: MARGIN, size: 'A4', bufferPages: true });
  const bufferPromise = collectBuffer(doc);

  registerFooter(doc, ctx);
  addBrandedHeader(doc, branding, 'Fee Payment Receipt');

  doc.moveDown(0.5);

  // Receipt meta
  addKeyValue(doc, 'Receipt No.', data.receipt_number);
  doc.moveDown(0.3);
  addKeyValue(doc, 'Student Name', data.student_name);
  doc.moveDown(0.3);
  addKeyValue(doc, 'Class', data.class_name);
  doc.moveDown(0.3);
  addKeyValue(
    doc,
    'Payment Date',
    data.payment_date.toLocaleDateString('en-IN', { day: '2-digit', month: 'short', year: 'numeric' }),
  );
  doc.moveDown(0.3);
  addKeyValue(doc, 'Payment Mode', data.payment_mode);
  doc.moveDown(0.8);

  // Fee head breakdown table
  doc.font('Helvetica-Bold').fontSize(11).text('Fee Breakdown', MARGIN, doc.y);
  doc.moveDown(0.3);

  const breakdownRows = data.fee_head_breakdown.map((fh) => [
    fh.name,
    `₹ ${fh.amount.toFixed(2)}`,
  ]);
  addTable(doc, ['Fee Head', 'Amount'], breakdownRows, [CONTENT_WIDTH * 0.7, CONTENT_WIDTH * 0.3]);

  // Total
  doc.moveDown(0.3);
  doc
    .font('Helvetica-Bold')
    .fontSize(11)
    .text(`Total Amount Paid: ₹ ${data.amount_paid.toFixed(2)}`, MARGIN, doc.y, {
      align: 'right',
      width: CONTENT_WIDTH,
    });

  doc.moveDown(1);

  // Non-refundable notice — red italic
  doc
    .font('Helvetica-Oblique')
    .fontSize(9)
    .fillColor('#cc0000')
    .text(
      'Please note: Fees once paid cannot be refunded under any circumstances.',
      MARGIN,
      doc.y,
      { align: 'center', width: CONTENT_WIDTH },
    );

  doc.fillColor('#000000');

  doc.end();
  return bufferPromise;
}

/**
 * Generate a staff salary payslip PDF.
 */
export async function generatePayslipPDF(
  data: PayslipData,
  branding: BrandingContext,
  ctx: GeneratorContext,
): Promise<Buffer> {
  const doc = new PDFDocument({ margin: MARGIN, size: 'A4', bufferPages: true });
  const bufferPromise = collectBuffer(doc);

  registerFooter(doc, ctx);
  addBrandedHeader(doc, branding, 'Salary Payslip');

  doc.moveDown(0.5);

  // Staff details table
  doc.font('Helvetica-Bold').fontSize(11).text('Staff Details', MARGIN, doc.y);
  doc.moveDown(0.3);
  addTable(
    doc,
    ['Name', 'Role', 'Employee ID', 'Month / Year'],
    [[data.staff_name, data.role, data.employee_id, data.month_year]],
    [
      CONTENT_WIDTH * 0.3,
      CONTENT_WIDTH * 0.2,
      CONTENT_WIDTH * 0.2,
      CONTENT_WIDTH * 0.3,
    ],
  );

  doc.moveDown(0.5);

  // Attendance summary
  doc.font('Helvetica-Bold').fontSize(11).text('Attendance Summary', MARGIN, doc.y);
  doc.moveDown(0.3);
  addTable(
    doc,
    ['Working Days', 'Present', 'Absent', 'Leaves'],
    [
      [
        String(data.working_days),
        String(data.present_days),
        String(data.absent_days),
        String(data.leave_days),
      ],
    ],
    [
      CONTENT_WIDTH * 0.25,
      CONTENT_WIDTH * 0.25,
      CONTENT_WIDTH * 0.25,
      CONTENT_WIDTH * 0.25,
    ],
  );

  doc.moveDown(0.5);

  // Earnings table
  doc.font('Helvetica-Bold').fontSize(11).text('Earnings', MARGIN, doc.y);
  doc.moveDown(0.3);
  const earningsRows = data.components.map((c) => [c.name, `₹ ${c.amount.toFixed(2)}`]);
  earningsRows.push(['Gross Total', `₹ ${data.gross_salary.toFixed(2)}`]);
  addTable(doc, ['Component', 'Amount'], earningsRows, [CONTENT_WIDTH * 0.7, CONTENT_WIDTH * 0.3]);

  doc.moveDown(0.5);

  // Deductions table
  doc.font('Helvetica-Bold').fontSize(11).text('Deductions', MARGIN, doc.y);
  doc.moveDown(0.3);
  const totalDeductions = data.deductions.reduce((sum, d) => sum + d.amount, 0);
  const deductionRows = data.deductions.map((d) => [d.name, `₹ ${d.amount.toFixed(2)}`]);
  deductionRows.push(['Total Deductions', `₹ ${totalDeductions.toFixed(2)}`]);
  addTable(doc, ['Deduction', 'Amount'], deductionRows, [CONTENT_WIDTH * 0.7, CONTENT_WIDTH * 0.3]);

  doc.moveDown(0.5);

  // Net salary — highlighted box
  const netY = doc.y;
  doc.rect(MARGIN, netY, CONTENT_WIDTH, 28).fill('#e8f5e9');
  doc.fillColor('#000000');
  doc
    .font('Helvetica-Bold')
    .fontSize(13)
    .text(`Net Salary: ₹ ${data.net_salary.toFixed(2)}`, MARGIN + 8, netY + 7, {
      width: CONTENT_WIDTH - 16,
      align: 'right',
    });

  doc.y = netY + 36;
  doc.moveDown(0.5);

  // Payment details
  doc.font('Helvetica-Bold').fontSize(11).text('Payment Details', MARGIN, doc.y);
  doc.moveDown(0.3);
  addKeyValue(doc, 'Payment Mode', data.payment_mode);
  doc.moveDown(0.3);
  addKeyValue(
    doc,
    'Payment Date',
    data.payment_date.toLocaleDateString('en-IN', { day: '2-digit', month: 'short', year: 'numeric' }),
  );

  doc.moveDown(1);

  // Disclaimer
  doc
    .font('Helvetica-Oblique')
    .fontSize(9)
    .fillColor('#555555')
    .text('This is a system-generated salary slip.', MARGIN, doc.y, {
      align: 'center',
      width: CONTENT_WIDTH,
    });

  doc.fillColor('#000000');

  doc.end();
  return bufferPromise;
}

/**
 * Generate a financial report PDF.
 */
export async function generateReportPDF(
  data: ReportData,
  branding: BrandingContext,
  ctx: GeneratorContext,
): Promise<Buffer> {
  const doc = new PDFDocument({ margin: MARGIN, size: 'A4', bufferPages: true });
  const bufferPromise = collectBuffer(doc);

  registerFooter(doc, ctx);
  addBrandedHeader(doc, branding, data.title);

  doc.moveDown(0.3);

  // Date range
  doc
    .font('Helvetica')
    .fontSize(10)
    .fillColor('#444444')
    .text(`Period: ${data.date_range}`, MARGIN, doc.y, { align: 'center', width: CONTENT_WIDTH });
  doc.fillColor('#000000');

  doc.moveDown(0.8);

  // Data table
  addTable(doc, data.headers, data.rows);

  doc.moveDown(0.8);

  // Summary section
  if (Object.keys(data.summary).length > 0) {
    doc.font('Helvetica-Bold').fontSize(11).text('Summary', MARGIN, doc.y);
    doc.moveDown(0.3);

    Object.entries(data.summary).forEach(([key, value]) => {
      addKeyValue(doc, key, value);
      doc.moveDown(0.3);
    });
  }

  doc.end();
  return bufferPromise;
}

/**
 * Generate an offer letter PDF.
 */
export async function generateOfferLetterPDF(
  data: OfferLetterData,
  branding: BrandingContext,
  ctx: GeneratorContext,
): Promise<Buffer> {
  const doc = new PDFDocument({ margin: MARGIN, size: 'A4', bufferPages: true });
  const bufferPromise = collectBuffer(doc);

  registerFooter(doc, ctx);
  addBrandedHeader(doc, branding, 'Offer Letter');

  doc.moveDown(0.8);

  const startDateStr = data.start_date.toLocaleDateString('en-IN', {
    day: '2-digit',
    month: 'long',
    year: 'numeric',
  });

  // Salutation
  doc
    .font('Helvetica')
    .fontSize(11)
    .text(`Dear ${data.staff_name},`, MARGIN, doc.y);

  doc.moveDown(0.5);

  doc.text(
    `We are pleased to offer you the position of ${data.role} at ${data.school_name}, ` +
      `effective from ${startDateStr}. Please find the details of your offer below.`,
    MARGIN,
    doc.y,
    { width: CONTENT_WIDTH },
  );

  doc.moveDown(0.8);

  // Salary breakdown table
  doc.font('Helvetica-Bold').fontSize(11).text('Compensation Breakdown', MARGIN, doc.y);
  doc.moveDown(0.3);

  const salaryRows = data.salary_breakdown.map((s) => [s.component, `₹ ${s.amount.toFixed(2)}`]);
  addTable(doc, ['Component', 'Amount (per month)'], salaryRows, [
    CONTENT_WIDTH * 0.65,
    CONTENT_WIDTH * 0.35,
  ]);

  doc.moveDown(0.8);

  // Employment terms
  doc.font('Helvetica-Bold').fontSize(11).text('Terms and Conditions', MARGIN, doc.y);
  doc.moveDown(0.3);
  doc
    .font('Helvetica')
    .fontSize(10)
    .text(data.employment_terms, MARGIN, doc.y, { width: CONTENT_WIDTH });

  doc.moveDown(1.5);

  // Signature block
  doc.font('Helvetica').fontSize(10).text('Authorised Signatory', MARGIN, doc.y);
  doc.text(data.school_name, MARGIN, doc.y + 2);

  doc.end();
  return bufferPromise;
}

/**
 * Generate an experience letter PDF.
 */
export async function generateExperienceLetterPDF(
  data: ExperienceLetterData,
  branding: BrandingContext,
  ctx: GeneratorContext,
): Promise<Buffer> {
  const doc = new PDFDocument({ margin: MARGIN, size: 'A4', bufferPages: true });
  const bufferPromise = collectBuffer(doc);

  registerFooter(doc, ctx);
  addBrandedHeader(doc, branding, 'Experience Letter');

  doc.moveDown(0.8);

  const startDateStr = data.start_date.toLocaleDateString('en-IN', {
    day: '2-digit',
    month: 'long',
    year: 'numeric',
  });
  const endDateStr = data.end_date.toLocaleDateString('en-IN', {
    day: '2-digit',
    month: 'long',
    year: 'numeric',
  });
  const issueDateStr = ctx.generated_at.toLocaleDateString('en-IN', {
    day: '2-digit',
    month: 'long',
    year: 'numeric',
  });

  // Date
  doc
    .font('Helvetica')
    .fontSize(10)
    .fillColor('#444444')
    .text(`Date: ${issueDateStr}`, MARGIN, doc.y, { align: 'right', width: CONTENT_WIDTH });
  doc.fillColor('#000000');

  doc.moveDown(0.5);

  // Salutation
  doc.font('Helvetica').fontSize(11).text(`To Whom It May Concern,`, MARGIN, doc.y);

  doc.moveDown(0.5);

  // Body
  doc.text(
    `This is to certify that ${data.staff_name} was employed with ${data.school_name} ` +
      `as ${data.role} from ${startDateStr} to ${endDateStr}.`,
    MARGIN,
    doc.y,
    { width: CONTENT_WIDTH },
  );

  doc.moveDown(0.5);

  doc.text(
    `During their tenure, ${data.staff_name} demonstrated professionalism and dedication. ` +
      `We wish them the very best in their future endeavours.`,
    MARGIN,
    doc.y,
    { width: CONTENT_WIDTH },
  );

  doc.moveDown(1.5);

  // Signature block
  doc.font('Helvetica').fontSize(10).text('Authorised Signatory', MARGIN, doc.y);
  doc.text(data.school_name, MARGIN, doc.y + 2);

  doc.end();
  return bufferPromise;
}
